Num1 = int(input("Enter the first number: "))
Num2 = int(input("Enter the second number: "))

if(Num1 > Num2):
    print(Num1, "is greater then", Num2)
elif(Num1 < Num2):
    print(Num2, "is greater then", Num1)
elif(Num1 == Num2):
    print(Num1, "is equal to", Num2)
else:
    print("Invalid....")